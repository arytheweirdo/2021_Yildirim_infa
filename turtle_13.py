import turtle

turtle.shape("turtle")
turtle.fillcolor("yellow")
turtle.begin_fill()
turtle.circle(100)
turtle.end_fill()
turtle.penup()
turtle.goto(-50, 90)
turtle.fillcolor("blue")
turtle.pendown()
turtle.begin_fill()
turtle.circle(20)
turtle.end_fill()
turtle.penup()
turtle.goto(50, 90)
turtle.pendown()
turtle.begin_fill()
turtle.circle(20)
turtle.end_fill()
turtle.penup()
turtle.goto(0, 80)
turtle.right(90)
turtle.pendown()
turtle.pencolor("black")
turtle.pensize(10)
turtle.forward(25)
turtle.penup()
turtle.goto(-30, 10)
turtle.pencolor("red")
turtle.pendown()
turtle.circle(30, -180)






